namespace {
nghttp2_nv makeNvInternal(const std::string &name, const std::string &value,
                          bool no_index, uint8_t nv_flags) 
{
   uint8_t flags;
   flags = nv_flags | (no_index ? NGHTTP2_NV_FLAG_NO_INDEX : NGHTTP2_NV_FLAG_NONE);

   return {
      (uint8_t *)name.c_str(), 
      (uint8_t *)value.c_str(), 
      name.size(),
      value.size(), 
      flags
   };
}
} // namespace

