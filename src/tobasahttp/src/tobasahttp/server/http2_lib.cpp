{
   return makeNvInternal(name, value, no_index, NGHTTP2_NV_FLAG_NONE);
}

nghttp2_nv makeNv(const StringRef &name, const StringRef &value, bool no_index) 
{
   return makeNvInternal(name, value, no_index, NGHTTP2_NV_FLAG_NONE);
}


